let treeCounter = 0;

// format the text on linebreaks for easy calculations
const formatMap = (map) => {
  return map.split(/\r?\n/g);
};

// the map repeats itself horizontally, so if we're at the end, start at the beginning with the rest value
const getXPosition = (arrLength, xPos, xStep) => {
  const newPosition = xPos + xStep;
  return newPosition <= arrLength - 1 ? newPosition : newPosition - arrLength;
};

// move recursive through the positions until we're at the bottom and count all trees
const getPosition = (formattedMap, steps, positions) => {
  treeCounter =
    formattedMap[positions.y].charAt(positions.x) === "#"
      ? treeCounter + 1
      : treeCounter;

  const newPosition = {
    x: getXPosition(formattedMap[0].length, positions.x, steps.x),
    y: (positions.y += steps.y),
  };

  formattedMap.length > positions.y &&
    getPosition(formattedMap, steps, newPosition);
};

// start moving through all positions and return the amount of trees
const partOne = (map) => {
  const steps = {
    x: 3,
    y: 1,
  };
  const formattedMap = formatMap(map);
  getPosition(formattedMap, steps, { x: 0, y: 0 });

  return treeCounter;
};

// start moving through all step types and their positions and multiply all amount of trees per step type
const partTwo = (map) => {
  const stepTypes = [
    {
      x: 1,
      y: 1,
    },
    {
      x: 3,
      y: 1,
    },
    {
      x: 5,
      y: 1,
    },
    {
      x: 7,
      y: 1,
    },
    {
      x: 1,
      y: 2,
    },
  ];
  const formattedMap = formatMap(map);

  return stepTypes.reduce((result, stepType) => {
    treeCounter = 0;
    getPosition(formattedMap, stepType, { x: 0, y: 0 });
    return result * treeCounter;
  }, 1);
};

export const Exercise3 = () => {
  return (
    <>
      <p>answer part 1: {partOne(map)}</p>
      <p>answer part 2: {partTwo(map)}</p>
    </>
  );
};
